            <br>布鲁日：比利时的古都，素有《小威尼斯》之称，是个迷人的中世纪小镇，旧市区内尽是古意盎然的石板道，以及文艺复兴时期的建筑。它是今日欧洲游览胜地也是近来浪漫的观光客趋之若骛的地方，BURGGE荷语是《桥》的意思，这个城里布满大大小小的运河，今日的布鲁日有50座以上的桥是标准的水上都市，布鲁日曾经因水而发展，也因河道淤塞而没落，却因此保有它的古朴之美，随着沿岸建筑搭配绿意藤蔓，仿若一幅幅山水名画至于眼前。
            <br>【马克特广场】：布鲁日古城的中心和最大的广场，广场四周围绕着15世纪极具魅力的荷兰风格的建筑，是欧洲屈指可数的优美建筑之一。【钟楼】：象征布鲁日的浪漫情调，建于13到15世纪，被公认为是比利时最棒的钟楼。7个多世纪以来，每15分钟，这里的钟就会敲响一次。【市政厅】：是充满哥德式建筑风格，建于公元1376年，是比利时最古老的市政厅。正面六扇尖顶穹窿窗垂直排列，造型新颖，别具特色。【圣血礼拜堂】：布鲁日最著名的教堂，堂内供奉着耶路撒冷圣地取回的基督圣血和遗物。每年4月底到5月中，是耶稣升天节，欧洲各界宗教人士在布鲁日聚会，顶礼膜拜救世主的圣血，成为比利时的重大宗教盛事。
