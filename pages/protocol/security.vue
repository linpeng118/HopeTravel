    </p><p class="txt-index">6. 视力不好的滑雪者，不要戴隐形眼镜滑雪，如果跌倒后隐形眼镜掉落，找回来的可能性几乎不存在。尽量配戴有边框的由树脂镜片制造的眼镜，它在受到撞击后不易碎裂。
