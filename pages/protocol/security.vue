    </p><p class="txt-index">1. 注意天气预报，避开大风天、暴雪天等恶劣天气。
    </p><p class="txt-index">2. 滑雪是较为复杂的运动，滑雪前要做一些简单的准备活动及热身运动，避免受伤。
